/**
 * add - Addition
 * @a: number 1
 * @b: number 2
 * Return: The sum of a and b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substraction
 * @a: number 1
 * @b: number 2
 * Return: sub of a and b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication
 * @a: number 1
 * @b: number 2
 * Return: multip of a and b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - modulo
 * @a: number 1
 * @b: number 2
 * Return: modulo of a and b
 */

int mod(int a, int b)
{
	return (a % b);
}

/**
 * div - division
 * @a: number 1
 * @b: number 2
 * Return: division of a and b
 */

int div(int a, int b)
{
	return (a / b);
}
